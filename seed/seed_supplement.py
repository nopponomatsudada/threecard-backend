"""
不足分の追加テーマを投入する補足スクリプト
各カテゴリ20テーマに到達させる
"""
import json, sys, time, urllib.request, urllib.error

BASE_URL = "http://localhost:8080/api/v1"
for arg in sys.argv[1:]:
    if arg.startswith("--base-url="):
        BASE_URL = arg.split("=", 1)[1]
    elif arg == "--base-url" and sys.argv.index(arg) + 1 < len(sys.argv):
        BASE_URL = sys.argv[sys.argv.index(arg) + 1]

def api(method, path, token=None, body=None):
    url = f"{BASE_URL}{path}"
    data = json.dumps(body).encode() if body else None
    headers = {"Content-Type": "application/json"}
    if token:
        headers["Authorization"] = f"Bearer {token}"
    for attempt in range(4):
        req = urllib.request.Request(url, data=data, headers=headers, method=method)
        try:
            with urllib.request.urlopen(req) as resp:
                return json.loads(resp.read())
        except urllib.error.HTTPError as e:
            err = e.read().decode()
            if e.code == 409: return None
            if e.code == 429:
                wait = 20 * (attempt + 1)
                print(f"    (429 → wait {wait}s)")
                time.sleep(wait)
                continue
            print(f"  ERR {e.code}: {err[:120]}")
            return None
    return None

# Get tokens - create 5 users with delays
tokens = []
for i in range(1, 6):
    if i > 1: time.sleep(13)
    r = api("POST", "/auth/device", body={"deviceId": f"seed-sup-{i}-{int(time.time())}"})
    if r and "data" in r:
        tokens.append(r["data"]["accessToken"])
        print(f"User {i}: OK")
    else:
        print(f"User {i}: FAIL")

print(f"\n{len(tokens)} tokens ready\n")
if len(tokens) < 3:
    sys.exit("Need 3+ users")

def seed(tag, title, desc, bests):
    """Create theme + 3 bests. bests = list of 3 lists of 3 (name,desc) tuples"""
    tid = None
    r = api("POST", "/themes", tokens[0], {"title": title, "description": desc, "tagId": tag})
    if r and "data" in r:
        tid = r["data"]["id"]
    else:
        print(f"  SKIP theme: {title[:30]}")
        return
    for j, items in enumerate(bests):
        api_items = [{"rank": k+1, "name": n, "description": d} for k, (n, d) in enumerate(items)]
        api("POST", f"/themes/{tid}/bests", tokens[(j+1) % len(tokens)], {"items": api_items})
    print(f"  ✓ {title[:50]}")

