def main():
    global BASE_URL
    if len(sys.argv) > 1 and sys.argv[1].startswith("--base-url"):
        BASE_URL = sys.argv[1].split("=")[1] if "=" in sys.argv[1] else sys.argv[2]

    print(f"=== threecard seed data loader ===")
    print(f"Base URL: {BASE_URL}")
    print()

    # 1. Create users
    print(f"[1/3] Creating {NUM_USERS} test users...")
    tokens = bootstrap_users(NUM_USERS)
    if len(tokens) < 3:
        print("ERROR: Need at least 3 users to create bests. Exiting.")
        sys.exit(1)
    print(f"  → {len(tokens)} users ready\n")

    # 2. Create themes and bests
    total_themes = sum(len(themes) for themes in THEMES.values())
    total_bests = sum(len(t[2]) for themes in THEMES.values() for t in themes)
    print(f"[2/3] Creating {total_themes} themes with {total_bests} bests...")

    theme_count = 0
    best_count = 0
    skip_count = 0

    for tag_id, themes in THEMES.items():
        print(f"\n  --- {tag_id} ({len(themes)} themes) ---")
        for i, (title, desc, bests) in enumerate(themes):
            # Rotate which user creates the theme
            creator_idx = i % len(tokens)
            theme_id = create_theme(tokens[creator_idx], tag_id, title, desc)
            if not theme_id:
                print(f"    SKIP: {title[:30]}...")
                skip_count += 1
                continue
            theme_count += 1
            print(f"    ✓ {title[:40]}...")

            # Create bests from different users
            for j, items in enumerate(bests):
                # Pick a different user for each best
                best_user_idx = (creator_idx + j + 1) % len(tokens)
                result = post_best(tokens[best_user_idx], theme_id, items)
                if result:
                    best_count += 1
                else:
                    skip_count += 1

    # 3. Summary
    print(f"\n[3/3] Done!")
    print(f"  Themes created: {theme_count}")
    print(f"  Bests created:  {best_count}")
    print(f"  Skipped:        {skip_count}")

    # Verify
    print("\n--- Verification ---")
    for tag_id in THEMES:
        resp = api("GET", f"/themes?tagId={tag_id}&limit=50", tokens[0])
        if resp and "data" in resp:
            count = len(resp["data"])
            print(f"  {tag_id}: {count} themes")

if __name__ == "__main__":
    main()
