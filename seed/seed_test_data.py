def api(method, path, token=None, body=None, retries=3):
    url = f"{BASE_URL}{path}"
    data = json.dumps(body).encode() if body else None
    headers = {"Content-Type": "application/json"}
    if token:
        headers["Authorization"] = f"Bearer {token}"
    for attempt in range(retries):
        req = urllib.request.Request(url, data=data, headers=headers, method=method)
        try:
            with urllib.request.urlopen(req) as resp:
                return json.loads(resp.read())
        except urllib.error.HTTPError as e:
            err = e.read().decode()
            if e.code == 409:
                return None
            if e.code == 429:
                wait = 15 * (attempt + 1)
                print(f"    (rate limited, waiting {wait}s...)")
                time.sleep(wait)
                continue
            print(f"  ERROR {e.code}: {err[:200]}")
            return None
    print(f"  FAILED after {retries} retries: {method} {path}")
    return None

def bootstrap_users(n):
    tokens = []
    for i in range(1, n + 1):
        # Rate limit for auth: 5/min → sleep between calls
        if i > 1 and (i - 1) % 4 == 0:
            print("    (pausing for auth rate limit...)")
            time.sleep(15)
        resp = api("POST", "/auth/device", body={"deviceId": f"seed-user-{i:03d}"})
        if resp and "data" in resp:
            tokens.append(resp["data"]["accessToken"])
            print(f"  User {i}: {resp['data']['user']['displayId']}")
        else:
            # user already exists — use a new device ID
            resp = api("POST", "/auth/device", body={"deviceId": f"seed-v2-{i:03d}-{int(time.time())}"})
            if resp and "data" in resp:
                tokens.append(resp["data"]["accessToken"])
                print(f"  User {i} (new): {resp['data']['user']['displayId']}")
            else:
                print(f"  User {i}: FAILED")
    return tokens

def create_theme(token, tag_id, title, description):
    resp = api("POST", "/themes", token, {
        "title": title,
        "description": description,
        "tagId": tag_id,
    })
    if resp and "data" in resp:
        return resp["data"]["id"]
    return None

def post_best(token, theme_id, items):
    """items: list of (name, description) tuples for rank 1,2,3"""
    api_items = []
    for rank, (name, desc) in enumerate(items, 1):
        item = {"rank": rank, "name": name}
        if desc:
            item["description"] = desc
        api_items.append(item)
    return api("POST", f"/themes/{theme_id}/bests", token, {"items": api_items})

